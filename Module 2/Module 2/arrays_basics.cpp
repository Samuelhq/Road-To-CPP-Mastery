/*This simple program explores the Static Arrays*/

#include <iostream>
using namespace std;

int main() {
	int myNumbers[5] = { 99, 234, -47, 29, 38 };
	cout << "First Number is: " << myNumbers[0] << endl;
	cout << "Second Number is: " << myNumbers[1] << endl;
	cout << "Third Number is: " << myNumbers[2] << endl;
	cout << "Fourth Number is: " << myNumbers[3] << endl;
	cout << "Fifth Number is: " << myNumbers[4] << endl;
	return 0;
}
